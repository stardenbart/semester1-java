public class DoWhileExample {

    public static void main(String[] args) {
        //DO WHILE LOOP
        int n = 5;
        int a = 0;
        System.out.println("Ini adalah awal program.");

        do{
            a++;
            System.out.println("Hello World!");
        }while(a < n);
        System.out.println("Ini adalah akhir program.");
    }
}
